export function getPlayer(id: number): Player | undefined {
  return PLAYERS.find((p) => p.id === id);
}

export function shuffleArray<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

// Auction set order for proper pool grouping
const SET_ORDER = [
  "WK-Set-1",
  "WK-Set-2",
  "WK-Set-3",
  "WK-Set-4",
  "Bat-Set-1",
  "Bat-Set-2",
  "Bat-Set-3",
  "Bowl-Set-1",
  "Bowl-Set-2",
  "Bowl-Set-3",
  "AR-Set-1",
  "AR-Set-2",
  "AR-Set-3",
];

export function createAuctionQueue(): number[] {
  const grouped: Record<string, number[]> = {};
  for (const set of SET_ORDER) grouped[set] = [];
  for (const p of PLAYERS) {
    if (grouped[p.auctionSet]) grouped[p.auctionSet].push(p.id);
    else grouped[p.auctionSet] = [p.id];
  }
  const queue: number[] = [];
  for (const set of SET_ORDER) {
    if (grouped[set]) queue.push(...shuffleArray(grouped[set]));
  }
  return queue;
}
